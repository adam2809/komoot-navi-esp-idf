bool isUuid128Equal(uint8_t a[ESP_UUID_LEN_128],uint8_t b[ESP_UUID_LEN_128]){
    for (int i = 0; i < ESP_UUID_LEN_128; i++){
        if (a[i] != b[i]){
            return false;
        }
    }
    return true;
    
}

